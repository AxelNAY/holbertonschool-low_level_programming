#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - do the sum of the two arguments.
 * @a: first number.
 * @b: second number.
 * Return: return the sum of a and b.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - do the substraction of the two arguments.
 * @a: first number.
 * @b: second number.
 * Return: return the substraction of a by b.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - do the multiplication of the two arguments.
 * @a: first number.
 * @b: second number.
 * Return: return the multiplication of a and b.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - do the division of the two arguments.
 * @a: first number.
 * @b: second number.
 * Return: return the division of a by b.
 */

int op_div(int a, int b)
{
	if (b == 0)
	{
		printf("Error\n");
		exit(100);
	}
	return (a / b);
}

/**
 * op_mod - do the modulo of the two arguments.
 * @a: first number.
 * @b: second number.
 * Return: return the modulo of a and b.
 */

int op_mod(int a, int b)
{
	if (b == 0)
	{
		printf("Error\n");
		exit(100);
	}
	return (a % b);
}
